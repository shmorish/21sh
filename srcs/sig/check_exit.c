#include "executor.h"
#include <sys/wait.h>

void	check_exit(int status)
{
	if (WIFEXITED(status))
		set_exit_status(WEXITSTATUS(status));
	else if (WIFSIGNALED(status))
		set_exit_status(WTERMSIG(status) + 128);
	else
		set_exit_status(1);
}
